package de.sollder1.chess.game.helpObjects;

//Represents a Point in the "Kartesisches Koordinaten System
public class Point{
	  
	private double x;
	private double y;
	  
	public Point(double x, double y){
		  
		this.x = x;
		this.y = y;
		  
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	  
	  
	  
	  
	  
}